// Pre-written answers for the common example questions shown on the homepage.
// Serving these from cache makes the reply instant, consistent (same answer
// every time — not a fresh AI generation), and free. Keyed by normalized text.

function normalize(q: string): string {
  return q.trim().replace(/\s+/g, " ").replace(/[?.!]/g, "").toLowerCase();
}

